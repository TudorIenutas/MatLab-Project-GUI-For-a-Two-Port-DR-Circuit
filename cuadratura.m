function sinus_callback(source,eventdata)
N=str2double(get(p3,'String'));
R=str2double(get(p2,'String'));
C=str2double(get(p1,'String'));
f=1/2*pi*R*C;
T=1/f;
t=0:T/200:N*T;
sinus=sin((2*pi*f*t));
subplot(2,2,2);
axis equal
plot(t,sinus,...
    'color','blue');
title('Signal Vout2 Chronogram');
grid on
end




function si_callback(sourc,eventdat)
N=str2double(get(p3,'String'));
R=str2double(get(p2,'String'));
C=str2double(get(p1,'String'));
f=1/2*pi*R*C;
T=1/f;
t=0:T/200:N*T;
sinus=sin((2*pi*f*t)+pi/2);
subplot(2,2,4)
axis equal
plot(t,sinus,...
    'color','red');
title('Signal Vout2 Chronogram');
grid on
end

